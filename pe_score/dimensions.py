"""
7维度35子指标定义 — PE投资项目评分体系

每个维度5个子指标，评分范围1-5（1=很差，5=优秀）
所有评分基于投资时点可获取的信息
"""

# 维度 → 子指标映射
DIMENSIONS = {
    "团队": [
        "创始人行业势能",    # 创始人在目标行业的积累深度和人脉
        "创始人操盘能力",    # 创始人从0到1的商业落地能力
        "团队拼图完整度",    # 技术/产品/销售/运营核心岗位覆盖
        "治理与激励",        # 股权结构、决策机制、团队激励
        "关键人风险",        # 核心人物离开的影响程度（1=依赖度极高，5=无依赖）
    ],
    "市场": [
        "赛道天花板",        # 目标市场总规模（TAM）
        "需求爆发时点",      # 需求是已爆发/即将爆发/遥远
        "竞争壁垒类型",      # 网络效应/技术壁垒/品牌/成本/无壁垒
        "客户粘性",          # 客户切换成本和复购意愿
        "下游分散度",        # 客户集中度（1=极集中单一客户，5=高度分散）
    ],
    "技术": [
        "技术原创度",        # 核心技术是自己研发还是集成/模仿
        "知识产权厚度",      # 专利数量、质量和保护范围
        "技术路线风险",      # 主流路线vs边缘路线，被颠覆的可能性
        "产业化成熟度",      # 实验室/中试/量产/规模化
        "研发追赶壁垒",      # 竞争对手复制所需时间和资源
    ],
    "财务": [
        "营收质量",          # 营收真实性、持续性、增长趋势
        "盈利路径",          # 盈利的时间表和可行性
        "资本效率",          # 每一元投入产生的营收/利润
        "现金流健康度",      # 经营性现金流、烧钱速度、现金储备
        "财务规范性",        # 审计、关联交易、财务透明度
    ],
    "退出": [
        "退出路径明确度",    # IPO/并购/回购的清晰程度
        "退出时间窗口",      # 当前资本市场窗口是否打开
        "监管适配度",        # 是否符合上市/并购的监管要求
        "估值锚定",          # 是否有可对标的公司确定合理估值
        "流动性预期",        # 退出后的流动性（交易量、减持难度）
    ],
    "政策": [
        "产业政策方向",      # 国家/地方政策是否支持该行业
        "补贴税收红利",      # 可获得的直接补贴和税收优惠
        "合规风险",          # 监管处罚、资质要求等风险（1=高风险，5=低风险）
        "国资LP适配度",      # 是否符合政府引导基金的投资偏好
        "地方政府支持",      # 地方政府在土地、税收、资源方面的支持
    ],
    "催化剂": [
        "短期催化事件",      # 6-12个月内可能发生的利好事件
        "估值弹性",          # 当前估值相对内在价值的上升空间
        "行业周期位置",      # 行业处于萌芽/上行/过热/下行哪个阶段
        "逆向信号",          # 警示风险的关键信号（1=强烈逆向，5=无逆向）
        "下轮融资预期",      # 下一轮融资的确定性和估值提升空间
    ],
}

# 扁平化维度名列表
DIM_NAMES = list(DIMENSIONS.keys())

# 扁平化子指标名列表（格式: 维度_子指标）
SUB_NAMES = []
for dim, subs in DIMENSIONS.items():
    for sub in subs:
        SUB_NAMES.append(f"{dim}_{sub}")

# 子指标索引映射: 维度名 → (start_index, end_index)
DIM_INDICES = {}
idx = 0
for dim, subs in DIMENSIONS.items():
    DIM_INDICES[dim] = (idx, idx + len(subs))
    idx += len(subs)
